<div class="box">
	<div class="box-header">
		<h2>
			<i class="fa fa-warning"></i>
			LAN Rules
		</h2>
	</div>
	<div class="box-content">
		<p>Please ensure you read through these rules, failure to abide by them may result in your dimissal from the LAN (without a refund) and/or being banned from future LANs</p>

		<h3>General</h3>
		<ul>
			<li>StaffsVGS accepts no responsibility for attendants' personal belongings. We will actively work to prevent any damage or theft, but the ultimate responsibility is with you.</li>
			<li>Your equipment is not covered by insurance unless it is your own.</li>
			<li>All injuries should be reported to a volunteer or committee member, regardless of severity.</li>
			<li>Please ensure your contact information, next of kin information and medical details are up to date on the Online Check-in.</li>
			<li>Photographs and Videos will be taken during the LAN and will be used for promotional purposes in the future.</li>
		</ul>

		<h3>Before the LAN</h3>
		<ul>
			<li>Transport
				<ul>
					<li>It is your responsibility to travel to and from the LAN safely.</li>
					<li>Providing you book it, we can provide <strong>equipment transport</strong> at &pound;1 per person per journey within the Stafford area, providing you have opted in to this service during online check-in.</li>
				</ul>
			</li>
			<li>Equipment
				<ul>
					<li>You are expected to bring your own equipment (PC, mouse, keyboard, power cables, etc).</li>
					<li>We will provide you with a labelled ethernet cable for the event. Please do not bring your own as you will not be permitted to use it. All of our cables are labelled with seat IDs and can provide a 1000Mbps throughput.</li>
					<li>Please ensure your equipment is safe. If it is a danger to other attendees or their equipment you may be asked to remove it. <strong>A brief, unintrusive inspection of your equipment will be required before you may plug it in, in order to comply with fire safety requirements.</strong></li>
					<li>Please ensure you have Anti-Virus installed (we recommend <a href="">Microsoft Security Essentials</a>!") and up to date, as well as all Operating System updates and patches installed.
						<ul>
							<li>Windows 8/8.1 users will have MSE installed by default, so just make sure it's enabled!</li>
						</ul>
					</li>
				</ul>
			</li>
		</ul>

		<h3>During the LAN</h3>
		<ul>
			<li>Who to talk to
				<ul>
					<li>Your first point of call should always be our volunteers, who you can identify during the LAN by looking at the <a href="/map">Live MAP</a>. Their seats have an orange outline.</li>
					<li>If you have any problems with a volunteer, please let a member of the committee know.</li>
				</ul>
			</li>

			<li><strong><em>Do not</em></strong>
				<ul>
					<li>Do not engage in any prohibited activities, including but not limited to:
						<ul>
							<li>Using the network for any illegal activities.</li>
							<li>Consuming alcohol on site, please only do so in the Union bar</li>
							<li>Use or distribute any illegal substances</li>
						</ul>
					</li>
					<li>Do not turn up drunk or get drunk at the LAN</li>
					<li>Do not modify our infrastructure, including but not limited to moving extension leads or switches</li>
					<li>Do not plug in any unauthorised devices, such as switches, hubs or routers</li>
					<li>Do not use anyone else's equipment without permission</li>
					<li>Do not steal or damage anyone else's equipment</li>
					<li>Do not leave open top liquid containers near electrical equipment. You may use a screw-cap or sports bottle for drinks.</li>
					<li>Do not eat messy food at your seat, we are required to not have any food near university equipment if we wish to use the venue.</li>
					<li>Do not act irresponsibly or anti-socially.</li>
				</ul>
			</li>

			<li>Safety
				<ul>
					<li>If you become injured or feel ill at the LAN, please report to a volunteer or committee member immediately for assistance.</li>
					<li>Please make us aware of any medical conditions or allergies you may have. If you have an epipen you need to keep with you, please make us aware of this as well as its location.</li>
					<li>Ensure you are familiar with all emergency exits and make sure they are not obstructed at all times.</li>
					<li>Please ensure you sign in and out via the LAN site whenever you leave or return to the venue. We need to know where people are in the event of an emergency.</li>
				</ul>
			</li>
		</ul>

		<h3>Post-LAN</h3>
		<ul>
			<li>Please leave the LAN in a reasonably tidy state. We will go round with bin bags at the end of the event to collect any rubbish you may have.</li>
			<li>Getting Home
				<ul>
					<li>The equipment transport will be available for returning your equipment home, though please be aware that this may take some time. You may leave your equipment with us and make your way home and we will deliver your equipment to you promptly.</li>
					<li>If you need help getting home, ask around! Someone may be able to help.</li>
					<li>Don't drive tired!</li>
					<li>If you think you cannot make it home safely, please let us know and we will do what we can to help</li>
				</ul>
			</li>
		</ul>
	</div>
</div>

