		<h2>
			<i class="fa fa-fire-extinguisher"></i>
			Fire Safety &amp; Emergency Procedures
		</h2>
	</div>
	<div class="box-content">
		<p>In the event of a fire, all attendees should make their way towards the nearest fire exit in an orderly fashion. There are fire two exits from the venue, you will be directed to the appropriate exit by a member of staff.</p>

		<p>Our designated Fire Assembly Point is <strong>Point 3</strong> on the <a href="http://www.staffs.ac.uk/assets/Stafford%20Fire%20Assembly%20Map_tcm44-70109.pdf" target="_blank">Beaconside Campus Fire Assembly Points Map</a>. After exiting the venue, please make your way to this point immediately for registration.</p>

		<p>We ask that in such an emergency you do everything you are told by a member of staff (whether it be university staff, committee or volunteer) in order to ensure that we resolve any emergencies quickly and efficiently.</p>
